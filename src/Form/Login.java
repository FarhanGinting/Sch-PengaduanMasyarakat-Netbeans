    private void LB_LUPAPASSWORDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LB_LUPAPASSWORDMouseClicked
        Connection kon = Koneksi.KoneksiDb();
        String input = JOptionPane.showInputDialog("Masukkan nomor telepon:");

// Lakukan pengecekan di database
// Misalnya, ganti dengan SQL query yang sesuai untuk mencari data pengguna berdasarkan nomor telepon
        String sql = "SELECT * FROM tbl_masyarakat WHERE no_telepon = ?";
        try ( PreparedStatement ps = kon.prepareStatement(sql)) {
            ps.setString(1, input);

            try ( ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    // Pengguna ditemukan, tampilkan profil
                    String profil = "Nama: " + rs.getString("nama") + "\n"
                            + "Username: " + rs.getString("username") + "\n"
                            + "Password: " + rs.getString("password") + "\n"
                            + "No. Telepon: " + rs.getString("no_telepon");

                    // Tampilkan profil menggunakan JOptionPane
                    JOptionPane.showMessageDialog(null, profil, "Profil Pengguna", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Pengguna tidak ditemukan.");
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_LB_LUPAPASSWORDMouseClicked


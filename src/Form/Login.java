    private void BTN_LOGINActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_LOGINActionPerformed
        // TODO add your handling code here:
        Connection kon = Koneksi.KoneksiDb();
        int level = 0;
        int nik = 0;
        int idpetugas = 0;
        try {
            Statement st = kon.createStatement();
            String sql = "SELECT * FROM tbl_petugas where username ='" + TXT_USERNAME.getText() + "' and password = '" + TXT_PASSWORD.getText() + "'";
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                if (CB_INGATSAYA.isSelected()) {
                    String rememberedUsername = TXT_USERNAME.getText();
                    preferences.put("rememberedUsername", rememberedUsername);
//                    System.out.println("Username remembered: " + rememberedUsername);
                } else {
                    preferences.remove("rememberedUsername");
                }

                if (rs.getString("level").equals("petugas")) {
                    int idPetugasFromResultSet = rs.getInt("id_petugas");
                    

                    Dashboard FRD1 = new Dashboard(1, 0, idPetugasFromResultSet);
                    FRD1.setVisible(true);
                    this.dispose();
                } else if (rs.getString("level").equals("administrator")) {
                    int idPetugasFromResultSet = rs.getInt("id_petugas");
                    

                    Dashboard FRD2 = new Dashboard(2, 0, idPetugasFromResultSet);
                    FRD2.setVisible(true);
                    this.dispose();
                }
            } else {
                Statement st2 = kon.createStatement();
                String sql2 = "SELECT * FROM tbl_masyarakat where username ='" + TXT_USERNAME.getText() + "' and password = '" + TXT_PASSWORD.getText() + "'";
                ResultSet rs2 = st2.executeQuery(sql2);

                if (rs2.next()) {
                    if (CB_INGATSAYA.isSelected()) {
                        String rememberedUsername = TXT_USERNAME.getText();
                        preferences.put("rememberedUsername", rememberedUsername);
                        //System.out.println("Username remembered: " + rememberedUsername);
                    } else {
                        preferences.remove("rememberedUsername");
                    }
                    Dashboard FRD3 = new Dashboard(3, rs2.getInt("nik"), 0);

                    FRD3.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Maaf password atau username akun anda salah");
                    TXT_USERNAME.setText("");
                    TXT_PASSWORD.setText("");
                    TXT_USERNAME.requestFocus();
                }
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_BTN_LOGINActionPerformed


    public TambahPengaduan(int level, int nik, int idpetugas) {
        initComponents();
        this.nik = nik;
        this.level = level;
        this.idpetugas = idpetugas;
        Calendar calendar = Calendar.getInstance();
        Date today = calendar.getTime();

// Mengatur nilai TGL_PENGADUAN dengan tanggal hari ini
        TGL_PENGADUAN.setDate(today);
        spTable.setVerticalScrollBar(new ScrollBar());
        spTable.getVerticalScrollBar().setBackground(Color.WHITE);
        spTable.getViewport().setBackground(Color.WHITE);

        // Mengganti query SQL untuk mendapatkan nama masyarakat
        try {
            Connection kon = Koneksi.KoneksiDb();
            String query = "SELECT tbl_pengaduan.id_pengaduan, tbl_pengaduan.tgl_pengaduan, tbl_masyarakat.nama, tbl_pengaduan.isi_laporan, tbl_pengaduan.status FROM tbl_pengaduan INNER JOIN tbl_masyarakat ON tbl_pengaduan.nik = tbl_masyarakat.nik WHERE tbl_pengaduan.nik = ?";
            try ( PreparedStatement pstmt = kon.prepareStatement(query)) {
                pstmt.setInt(1, nik);
                try ( ResultSet rs = pstmt.executeQuery()) {
                    if (rs.next()) {
                        String namaMasyarakat = rs.getString("nama");
                        TXT_NIK.setText("MYRK               :   " + namaMasyarakat);
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

        TXT_PATHFOTO.setEditable(false);

        BTN_CANCEL.addActionListener(e -> {
            PanelHome form1 = new PanelHome(level, nik, idpetugas);
            Dashboard dashboard = (Dashboard) SwingUtilities.getWindowAncestor(this);

            if (dashboard != null) {
                dashboard.setForm(form1);
            }
        });
    }

    public void setFotoFromFileChooser() {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("'.IMAGE", "jpg", "gif", "png");
        fileChooser.addChoosableFileFilter(filter);
        int result = fileChooser.showSaveDialog(null);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            TXT_PATHFOTO.setText(path);

            try {
                // blob => ImageIcon
                byte[] img = Files.readAllBytes(selectedFile.toPath());
                ImageIcon imageIcon = new ImageIcon(img);
                fotoByteArray = Files.readAllBytes(selectedFile.toPath());

                // UKURAN YANG AKAN DI TAMPILKAN
                int labelWidth = 320;
                int labelHeight = 320;

                // UKURAN GAMBAR ASLI
                int imageWidth = imageIcon.getIconWidth();
                int imageHeight = imageIcon.getIconHeight();

                // HITUNG SKALA UNTUK GAMBAR YANG BARU
                double scaleX = (double) labelWidth / (double) imageWidth;
                double scaleY = (double) labelHeight / (double) imageHeight;
                double scale = Math.min(scaleX, scaleY);

                //UBAH UKURAN GAMBAR DENGAN SKALA YANG SUDAH DI HITUNG
                Image scaledImage = imageIcon.getImage().getScaledInstance((int) (scale + imageWidth), (int) (scale + imageHeight), Image.SCALE_SMOOTH);

                // MENAMPILKAN GAMBAR KE LABEL
                lb_gambar.setIcon(new ImageIcon(new ImageIcon(scaledImage).getImage().getScaledInstance(320, 320, Image.SCALE_SMOOTH)));

            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }


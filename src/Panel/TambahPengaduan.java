    private void BTN_TAMBAHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_TAMBAHActionPerformed
        Connection kon = Koneksi.KoneksiDb();
        String tglPengaduan = getFormattedCurrentDate(); // Mengasumsikan Anda memiliki metode untuk mendapatkan tanggal saat ini yang diformat
        String isiLaporan = TXT_LAPORAN.getText(); // Mengasumsikan Anda memiliki kolom teks untuk isi_laporan

        // Tambahkan kolom 'nik' ke query dan gunakan "RETURN_GENERATED_KEYS"
        String sql = "INSERT INTO tbl_pengaduan (id_pengaduan, nik, tgl_pengaduan, isi_laporan, foto, status) VALUES (DEFAULT, ?, ?, ?, ?, '0')";

        try ( PreparedStatement ps = kon.prepareStatement(sql, PreparedStatement.RETURN_GENERATED_KEYS)) {
            ps.setInt(1, nik); // Mengasumsikan Anda memiliki nilai 'nik' yang tersedia
            ps.setString(2, tglPengaduan);
            ps.setString(3, isiLaporan);
            ps.setBytes(4, fotoByteArray);

            int result = ps.executeUpdate();

            if (result > 0) {
                // Dapatkan nilai auto-increment yang dihasilkan
                try ( ResultSet generatedKeys = ps.getGeneratedKeys()) {
                    if (generatedKeys.next()) {
                        int generatedIdPengaduan = generatedKeys.getInt(1);
                        JOptionPane.showMessageDialog(null, "Pengaduan berhasil disimpan dengan ID: " + generatedIdPengaduan);
                        // Lakukan hal-hal lain setelah penyimpanan sukses
                        PanelHome form1 = new PanelHome(level, nik, idpetugas);
                        Dashboard dashboard = (Dashboard) SwingUtilities.getWindowAncestor(this);

                        if (dashboard != null) {
                            dashboard.setForm(form1);
                        }
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Pengaduan gagal disimpan. Silakan coba lagi.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_BTN_TAMBAHActionPerformed

